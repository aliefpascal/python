def death_rate(t05=1, dt=1):
  """
  0.5 = (1 - d * dt)**(t05/dt)
  0.5**(dt/t05) = 1 - d * dt
  d * dt = 1 - 0.5**(dt/t05)
  """
  d = (1 - 0.5**(dt/t05)) / dt
  return d

t05 = 4
dt = 0.5
d = death_rate(t05=t05, dt=dt)
N = 400

print("N0 =", N)
print("t05 =", t05)
print("dt =", dt)
print("d =", d)
print()

Nt = 26
print("i\tt\tN")
for i in range(Nt):
  t = i * dt
  print(f"{i}\t{t:.1f}\t{N:.3f}")
  dN = d * N
  N -= dN * dt

