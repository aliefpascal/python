# popul_birth.py

def birth_rate(t2=1, dt=1):
  """
  2 = (1 + b * dt)**(t2/dt)
  2**(dt/t2) = 1 + b * dt
  b * dt = 2**(dt/t2) - 1
  """
  b = (2**(dt/t2) - 1) / dt
  return b

t2 = 2
dt = 0.25
b = birth_rate(t2=t2, dt=dt)
N = 1.5

print("N0 =", N)
print("t2 =", t2)
print("dt =", dt)
print("b =", b)
print()

Nt = 26
print("i\tt\tN")
for i in range(Nt):
  t = i * dt
  print(f"{i}\t{t:.2f}\t{N:.3f}")
  dN = b * N
  N += dN * dt

